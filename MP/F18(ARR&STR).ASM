DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
MSG DB 'ENTER YOUR NAME:'       ;MESSAGE FOR INPUT
ARRAY DB 100 DUP('$')           ;ARRAY FOR NAME
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
MOV DX,OFFSET MSG
MOV AH,9
INT 21H
MOV SI,OFFSET ARRAY             ;MOVING FIRST ADDRESS OF ARRAY

NAME_LOOP:                      ;INPUT LOOP
MOV AH,1
INT 21H
CMP AL,13                       ;COMPARE CONDITION
JE NAME_PRINT                   ;JUMP STATEMENT

MOV [SI],AL                     ;MOVING INPUT TO ARRAY ADDRESS
INC SI
JMP NAME_LOOP

NAME_PRINT:                     ;CONDITIONAL JUMP
MOV DX,OFFSET ARRAY
MOV AH,9
INT 21H
MAIN ENDP
END MAIN













