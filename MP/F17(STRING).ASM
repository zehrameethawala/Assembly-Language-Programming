DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
VAR1 DB 100 DUP('$')          ;INITIALIZE AN ARRAY
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
MOV SI,OFFSET VAR1
L1:
MOV AH,1                      ;TAKING INPUT
INT 21H
CMP AL,13
JE PROGRAMMEND
MOV [SI],AL
INC SI
JMP L1
PROGRAMMEND:
MOV DX,OFFSET VAR1            ;PRINTING STRING
MOV AH,9
INT 21H
MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN
