DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
Q DW ?                 ;VARIABLE FOR QUOTIENT
R DW ?                 ;VARIABLE FOR REMAINDER

.CODE
MAIN PROC
MOV AX,@DATA
MOV AX,37              ;DIVIDEND
MOV DX,0               ;REGISTER FOR REMAINDER
MOV BX,7               ;DIVISOR
DIV BX

MOV Q,AX              ;MOVING QUOTIENT TO Q
MOV R,DX              ;MOVING REMAINDER TO R

MOV DX,Q              ;PRINT QUOTIENT
ADD DX,48
MOV AH,2
INT 21H

MOV DX,10
MOV AH,2
INT 21H
MOV DX,13
MOV AH,2
INT 21H

MOV DX,R              ;PRINT REMAINDER
ADD DX,48
MOV AH,2
INT 21H

MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN




