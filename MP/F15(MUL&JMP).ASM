DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
MSG DB 'ENTER A NUMBER FOR MULTIPLICATION(0-9):$'
NUM DB ?
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
MOV DX,OFFSET MSG
MOV AH,9
INT 21H

MOV AH,1
INT 21H
SUB AL,'0'
MOV NUM,AL            ;STORE INPUT IN VARIABLE NUM
MOV DX,10
MOV AH,2
INT 21H

MOV BL,1              ;INITIAL MULTIPLIER BY 1

L1:
MOV AL,NUM 
MUL BL                ;MULTIPLY AL BY BL
AAM
MOV CH,AH             ;MOVE VALUE OF AH IN CH
MOV CL,AL             ;MOVE VALUE OF AL IN CL
MOV DL,BL             ;PRINT MULTIPLIER
ADD DL,48
MOV AH,2
INT 21H
MOV DL,'*' 
MOV AH,2
INT 21H
MOV DL,NUM            ;PRINT MULTIPLICANT
ADD DL,48
MOV AH,2
INT 21H
MOV DL,'='
MOV AH,2
INT 21H
MOV DL,CH             ;PRINT FIRST VALUE
ADD DL,48
MOV AH,2
INT 21H
MOV DL,CL             ;PRINT SECOND VALUE
ADD DL,48
MOV AH,2
INT 21H
MOV DX,10             ;NEWLINE CHARACTER
MOV AH,2
INT 21H
ADD BL,1              ;INCREMENT MULTIPLIER
CMP BL,9              ;COMPARE MULTIPLIER TO 9
JLE L1                ;JUMP IF MULTIPLIER IS <= 9

MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN
