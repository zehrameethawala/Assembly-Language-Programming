DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
MSG1 DB 'STRING:$'
MSG2 DB 'REVERSE STRING:$'
STRING DB 'ABC$'
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
MOV DX,OFFSET MSG1          ;PRINT MESSAGE 1
MOV AH,9
INT 21H
MOV DX,OFFSET STRING        ;PRINT STRING
MOV AH,9
INT 21H
MOV DX,10
MOV AH,2
INT 21H
MOV DX,OFFSET MSG2          ;PRINT MESSAGE 2
MOV AH,9
INT 21H

MOV SI,OFFSET STRING        ;SEND SI TO FIRST ADDRESS OF STRING

MOV CX,3                    ;SET COUNTER ACCORDING TO LENGTH
L1:
MOV BX,[SI]                 ;MOVE ELEMENT TO BX
PUSH BX                     ;PUSH BX INTO STACK
INC SI
LOOP L1                     ;LOOP CALL

MOV CX,3                    ;SET COUNTER ACCORDING TO LENGTH
L2:
POP DX                      ;POP ELEMENT FROM STACK
MOV AH,2
INT 21H
LOOP L2                     ;LOOP CALL

MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN

