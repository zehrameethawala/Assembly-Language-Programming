DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
.CODE
MAIN PROC
MOV CX,10           ;COUNTER FOR LOOP
MOV DX,48           ;INITIAL CONDIITION

LOOP1:              ;LOOP DECLARATION
MOV AH,2
INT 21H
INC DX              ;INCREMENT

LOOP LOOP1          ;CALL FOR LOOP

MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN

