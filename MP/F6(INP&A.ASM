DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
.CODE
MAIN PROC

MOV AH,1           ;INPUT 1ST NUMBER
INT 21H
MOV BL,AL          ;MOVING NUMBER TO BL

MOV AH,1           ;INPUT 2ND NUMBER
INT 21H

ADD BL,AL          ;ADDING NUMBERS
SUB BL,48          ;FOR ASCII VALUE
MOV DL,BL
MOV AH,2
INT 21H

MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN
