DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
ARRAY DB 'A','B','C'      ;INITIALIZING AN ARRAY
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
MOV SI,OFFSET ARRAY               ;MOVING STARTING ADDRESS OF ARRAY 
MOV CX,3
L1:                               ;LOOP INITIALIZATION
MOV DX,[SI]
MOV AH,2
INT 21H
INC SI                            ;INCREMENT IN SI VALUE
LOOP L1                           ;CALL FOR LOOP
MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN

