DOSSEG                  
.MODEL SMALL
.STACK 100H
.DATA
MSG DB 'ENTER A CHARACTER:$'
VAR DB ?

.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX

MOV DX,OFFSET MSG            ;PRINT STRING
MOV AH,9
INT 21H

MOV AH,1                     ;INPUT CHARACTER
INT 21H
MOV BL,AL
MOV VAR,BL                   ;ASSIGNING VALUE TO VARIABLE

MOV DX,10                    ;NEWLINE FEED
MOV AH,2
INT 21H

MOV DX,13                    ;CARRIAGE RETURN
MOV AH,2
INT 21H

MOV DL,VAR
MOV AH,2                     ;PRINT CHARACTER
INT 21H

MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN
