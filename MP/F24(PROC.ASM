PRINT MACRO P1                        ;MACRO STRUCTURE
MOV DX,OFFSET P1
MOV AH,9
INT 21H
ENDM

.MODEL SMALL
.STACK 100H
.DATA
MSG1 DB 'ENTER FIRST NAME:'
STR1 DB 100 DUP('$')
MSG2 DB 'ENTER LAST NAME:'
STR2 DB 100 DUP('$')

.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
PRINT MSG1                            ;MACRO CALL
MOV SI,OFFSET STR1
FIRST:                                ;LOOP FOR FIRST NAME
MOV AH,1
INT 21H
CMP AL,13
JE L1
MOV [SI],AL
INC SI
JMP FIRST

L1:
CALL ENTERKEY                         ;PROCEDURE CALL
PRINT MSG2                            ;MACRO CALL
MOV SI,OFFSET STR2
LAST:                                 ;LOOP FOR LAST NAME
MOV AH,1
INT 21H
CMP AL,13
JE PRINTING
MOV [SI],AL
INC SI
JMP LAST

PRINTING:
CALL ENTERKEY                         ;PROCEDURE CALL
PRINT STR1                            ;MACRO CALL
MOV DX,' '
MOV AH,2                              
INT 21H
PRINT STR2                            ;MACRO CALL
MOV AH,4CH
INT 21H
MAIN ENDP

ENTERKEY PROC                         ;PROCEDURE STRUCTURE
MOV DX,10
MOV AH,2
INT 21H
MOV DX,13
MOV AH,2
INT 21H
RET
ENTERKEY ENDP
END MAIN



